package com.uniminuto.simulacion.simu.pojo;

public class ListasString  {
	private String[] X,U;

	
	public ListasString(String[] x, String[] u) {
		super();
		X = x;
		U = u;
	}

	public String[] getX() {
		return X;
	}

	public void setX(String[] x) {
		X = x;
	}

	public String[] getU() {
		return U;
	}

	public void setU(String[] u) {
		U = u;
	}
	
	
}
