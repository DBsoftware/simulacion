package com.uniminuto.simulacion.simu.pojo;

public class Listas {
	private double[] X,U;

	
	public Listas(double[] x, double[] u) {
		super();
		X = x;
		U = u;
	}

	public double[] getX() {
		return X;
	}

	public void setX(double[] x) {
		X = x;
	}

	public double[] getU() {
		return U;
	}

	public void setU(double[] u) {
		U = u;
	}
	
	
}
